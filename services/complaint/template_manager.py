def get_template_key(case_type: str, is_minor: bool) -> str:
    """根据案件类型和是否未成年，返回模板键名。

    两种案件类型各有 adult/minor 两个变体。
    旧 neonatal 兼容：统一走 injury 路径。
    """
    # 旧 neonatal 兼容：统一走 injury 路径
    if case_type == "neonatal":
        case_type = "injury"
        is_minor = True

    if case_type == "injury":
        return "injury_minor" if is_minor else "injury_adult"
    elif case_type == "death":
        return "death_minor" if is_minor else "death_adult"
    else:
        # 兜底：未知类型按伤残处理
        return "injury_minor" if is_minor else "injury_adult"


def get_template(template_key: str) -> dict[str, Any]:
    return TEMPLATE_REGISTRY.get(template_key, TEMPLATE_REGISTRY["injury_adult"])
